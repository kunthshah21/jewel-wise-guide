// Helper Functions

export const getPendantsByLifecycleStage = (stage: PendantItem["lifecycleStage"]) => {
  return pendantInventory.filter((item) => item.lifecycleStage === stage);
};

export const getPendantsByType = (type: string) => {
  return pendantInventory.filter((item) => item.type === type);
};

export const getPendantsByMetal = (metal: string) => {
  return pendantInventory.filter((item) => item.metal === metal);
};

export const getPendantsByLocation = (location: string) => {
  return pendantInventory.filter((item) => item.stockLocation === location);
};

export const getPendantsByDesignStyle = (style: string) => {
  return pendantInventory.filter((item) => item.designStyle === style);
};

export const getTotalStockValue = () => {
  return pendantInventory.reduce((sum, item) => sum + item.finalSellingPrice, 0);
};

export const getAverageDaysInInventory = () => {
  const total = pendantInventory.reduce((sum, item) => sum + item.daysInInventory, 0);
  return Math.round(total / pendantInventory.length);
};

export const getStockValueByLifecycle = () => {
  const stages = ["Fast-moving", "Slow-moving", "Ageing Stock", "Dead Stock", "New Arrival"];
  return stages.map((stage) => {
    const items = getPendantsByLifecycleStage(stage as PendantItem["lifecycleStage"]);
    const value = items.reduce((sum, item) => sum + item.finalSellingPrice, 0);
    return { stage, count: items.length, value };
  });
};

export const getStockDistributionByType = () => {
  const types = new Map<string, { count: number; value: number }>();
  pendantInventory.forEach((item) => {
    const existing = types.get(item.type) || { count: 0, value: 0 };
    types.set(item.type, {
      count: existing.count + 1,
      value: existing.value + item.finalSellingPrice,
    });
  });
  return Array.from(types.entries())
    .map(([type, data]) => ({ type, ...data }))
    .sort((a, b) => b.value - a.value);
};

