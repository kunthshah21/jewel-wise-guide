export const getAverageDaysInInventory = () => {
  const total = bangleInventory.reduce((sum, item) => sum + item.daysInInventory, 0);
  return Math.round(total / bangleInventory.length);
};

export const getStockValueByLifecycle = () => {
  const stages = ["Fast-moving", "Slow-moving", "Ageing Stock", "Dead Stock", "New Arrival"];
  return stages.map((stage) => {
    const items = getBanglesByLifecycleStage(stage as BangleItem["lifecycleStage"]);
    const value = items.reduce((sum, item) => sum + item.finalSellingPrice, 0);
    return { stage, count: items.length, value };
  });
};

export const getStockDistributionByType = () => {
  const types = new Map<string, { count: number; value: number }>();
  bangleInventory.forEach((item) => {
    const existing = types.get(item.type) || { count: 0, value: 0 };
    types.set(item.type, {
      count: existing.count + 1,
      value: existing.value + item.finalSellingPrice,
    });
  });
  return Array.from(types.entries())
    .map(([type, data]) => ({ type, ...data }))
    .sort((a, b) => b.value - a.value);
};

